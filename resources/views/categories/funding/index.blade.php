<p>A recent research article published by the B-BBEE Commissioner clearly shows that Enterprise and Supplier Development systems are not working optimally.</p>
<p>We have long discussed this as a team – and this year marks 25 years in&nbsp;<strong><a href="http://www.siyakha.co.za/">Siyakha</a>’s</strong>&nbsp;history and 21 years in our delivery of SMME and Community Development solutions.</p>
<p>The over-emphasis on “funding” solutions is often an indication of the absence of strong financial management. Last year our team undertook a mock exercise to apply for SMME funding across a multitude of SMME specialist funders.</p>
<p>The finding in all instances except two, was that SMME funding follows pretty much the same credit criteria as corporate funding. This means that entities that lack strong balance sheets and established financials will pay a premium which they can ill afford.</p>
<p>Our overwhelming findings in SMME development are that there are two areas where every small business needs support and assistance.</p>
<ul>
<li>The first is what might be termed “commercial” –<strong>&nbsp;financial management, pricing, negotiating supplier contracts, legal agreements with clients, governance, and compliance.</strong>&nbsp;In essence, all of these things drive business viability and longevity.</li>
</ul>
<ul>
<li>The second is&nbsp;<strong>sales</strong>. This includes a good elevator pitch, daily sales calls, simple but great proposals and a developed brand. It also involves using social media to support business visibility, having the basic tools for a strong corporate identity, and understanding when and how to use them to support the sales process.</li>
</ul>
<p><br>Solid commercials and a sales strategy, added to a great entrepreneurial mindset, are the essential ingredients. They may remove the need for funding altogether and certainly minimise the risks associated with funding.</p>
